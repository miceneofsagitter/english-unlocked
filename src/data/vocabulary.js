// Helper: converte un array di espressioni (campo `en`) nel formato standard
function _mapExpr(arr, language, type, tag) {
  return arr.map((e) => ({
    language,
    type,
    verb: e.en,
    emoji: e.emoji,
    it: e.it,
    example_en: '',
    example_it: '',
    tags: e.tags || [tag],
    context_note: e.context || '',
  }));
}

